package p01.basic4;

public class Student {
	String name;
	int age;
	boolean isComputerMajor;
	char gender;
	
	public Student() {	}
	
	public Student(String name, int age, boolean isComputerMajor, char gender) {
		super();
		this.name = name;
		this.age = age;
		this.isComputerMajor = isComputerMajor;
		this.gender = gender;
	}
	
	

}
